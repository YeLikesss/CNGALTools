        /// <summary>
        /// dest ^= key
        /// <para>HappyLiveShowup.dll(v102) RVA 0x61970(.text)</para>
        /// </summary>
        /// <param name="dest">目标数据</param>
        /// <param name="key">源数据</param>
        /// <param name="position">起始位置</param>
        /// <returns>加解密长度</returns>
        public static int Xor(in Span<byte> dest, in ReadOnlySpan<byte> key, long position)
        {
            if (key.Length == 0)
            {
                return 0;
            }

            int idx = (int)(position % key.Length);
            int len = dest.Length;
            for (int i = 0; i < len; ++i)
            {
                dest[i] ^= key[idx];

                ++idx;
                if (idx == key.Length)
                {
                    idx = 0;
                }
            }
            return len;
        }

